import { Component } from '@angular/core';
import {CdkDragDrop, moveItemInArray, transferArrayItem} from '@angular/cdk/drag-drop';
@Component({
  selector: 'app-seller-orders',
  templateUrl: './seller-orders.component.html',
  styleUrls: ['./seller-orders.component.css']
})
export class SellerOrdersComponent {
  pending = ['Get to work', 'Pick up groceries', 'Go home', 'Fall asleep'];

  processing = ['Get up', 'Brush teeth', 'Take a shower', 'Check e-mail', 'Walk dog'];
  shipped = ['Get up', 'Brush teeth', 'Take a shower', 'Check e-mail', 'Walk dog'];
  delivered = ['Get up', 'Brush teeth', 'Take a shower', 'Check e-mail', 'Walk dog'];

  drop(event: CdkDragDrop<string[]>) {
    if (event.previousContainer === event.container) {
      moveItemInArray(event.container.data, event.previousIndex, event.currentIndex);
    } else {
      transferArrayItem(
        event.previousContainer.data,
        event.container.data,
        event.previousIndex,
        event.currentIndex,
      );
    }
  }
}
